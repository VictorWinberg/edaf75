-- We disable foreign key checks temporarily so we can delete the
-- tables in arbitrary order, and so insertion is faster.

PRAGMA foreign_keys = off;

-- Drop the tables if they already exist.

DROP TABLE IF EXISTS users;
CREATE TABLE users (
  username        TEXT NOT NULL,
  name            TEXT NOT NULL,
  address         TEXT,
  phone           TEXT NOT NULL,
  PRIMARY KEY     (username)
);

DROP TABLE IF EXISTS movies;
CREATE TABLE movies (
  title           TEXT NOT NULL,
  PRIMARY KEY     (title)
);

DROP TABLE IF EXISTS theaters;
CREATE TABLE theaters (
  name            TEXT NOT NULL,
  seats           INTEGER NOT NULL CHECK (seats > 0),
  PRIMARY KEY     (name)
);

DROP TABLE IF EXISTS shows;
CREATE TABLE shows (
  movie           TEXT NOT NULL,
  theater         TEXT NOT NULL,
  date            DATE NOT NULL,
  free_seats INTEGER NOT NULL CHECK (free_seats >= 0),
  PRIMARY KEY     (movie, date),
  FOREIGN KEY     (movie) REFERENCES movies(title),
  FOREIGN KEY     (theater) REFERENCES theaters(name)
);

DROP TABLE IF EXISTS reservations;
CREATE TABLE reservations (
  reservation_id  INTEGER,
  username        TEXT NOT NULL,
  movie           TEXT NOT NULL,
  date            TEXT NOT NULL,
  PRIMARY KEY     (reservation_id),
  FOREIGN KEY     (username) REFERENCES users(username),
  FOREIGN KEY     (movie, date) REFERENCES shows(movie, date)
);

